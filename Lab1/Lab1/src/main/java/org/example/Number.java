package org.example;

public class Number {
    private int real=0;
    private int imag=0;

    public void setReal(int real) {
        this.real = real;
    }

    public void setImag(int imag){
        this.imag=imag;
    }

    public int getImag() {
        return imag;
    }

    public int getReal() {
        return real;
    }
}
